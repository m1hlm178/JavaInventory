    private void AddCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddCatActionPerformed
    if(TXTName.getText().isEmpty() == true)
    {
        JOptionPane.showMessageDialog(null, "Your Data Is Not Complete!!");
    }
    else
    {
                    try {
                            String GenerateIDCat = AllClass.GenerateID("category", "idCategory");
                            // TODO add your handling code here:
                            Conn.rowSet.setCommand("select * from category");
                            Conn.rowSet.execute();
                            Conn.rowSet.moveToInsertRow();
                            Conn.rowSet.updateString("idCategory", GenerateIDCat);
                            Conn.rowSet.updateString("Name", TXTName.getText());
                            Conn.rowSet.insertRow();
                            updateTable();
                    } catch (SQLException ex) {
                            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                    }
    }
        clear();
        // TODO add your handling code here:
    }//GEN-LAST:event_AddCatActionPerformed

