    private void makeCenter() {
		Dimension frameSize = this.getSize();
		if (frameSize.height > 594) {
			frameSize.height = 594;
		}
		if (frameSize.width > 900) {
			frameSize.width = 900;
		}
		this.setLocation((900 - frameSize.width) / 2, (594 - frameSize.height) / 2);
	}

