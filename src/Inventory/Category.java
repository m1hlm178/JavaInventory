    public void updateTable(){
		try {
                        DefaultTableModel Tabel = (DefaultTableModel) TableCat.getModel();
                        while(Tabel.getRowCount() > 0) Tabel.removeRow(0);
			Conn.rowSet.setCommand("select * from category");
			Conn.rowSet.execute();
                        while(Conn.rowSet.next())
                        {
                            Tabel.addRow(new Object[]{Conn.rowSet.getString("idCategory"),Conn.rowSet.getString("Name")});
                        }
		} catch (SQLException ex) {
			Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
		}
    }
    
