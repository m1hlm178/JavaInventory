    private void UpdateCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateCatActionPerformed
    if(TXTName.getText().isEmpty() == true)
    {
        JOptionPane.showMessageDialog(null, "Please Select Your Data First!!");
    }
    else
    {
            try{
                int baris = TableCat.getSelectedRow();
                Conn.rowSet.setCommand("select * from category");
                Conn.rowSet.execute();
                Conn.rowSet.absolute(baris+1);
//                Conn.rowSet.updateString("idCategory", LabelID.getText());
                Conn.rowSet.updateString("Name", TXTName.getText());
                Conn.rowSet.updateRow();
                updateTable();
                } catch (SQLException ex) {
                Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
                clear();
        // TODO add your handling code here:
    }//GEN-LAST:event_UpdateCatActionPerformed

    private void DeleteCatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteCatActionPerformed
        if(TXTName.getText().isEmpty() == true)
        {
            JOptionPane.showMessageDialog(null, "Please Select Your Data First!!");
        }
        else
        {
        try {
        // TODO add your handling code here:
        int baris = TableCat.getSelectedRow();
        Conn.rowSet.setCommand("select * from category");
        Conn.rowSet.execute();
        Conn.rowSet.absolute(baris+1);
        Conn.rowSet.deleteRow();
        updateTable();
        } catch (SQLException ex) {
                Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Please Delete All Inventory With this Category First!!","ERROR!!",JOptionPane.ERROR_MESSAGE);
        }
        }
        clear();
        // TODO add your handling code here:
    }//GEN-LAST:event_DeleteCatActionPerformed

