    private void AddButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButActionPerformed
        if(TXTName.getText().isEmpty() == true || TXTAddress.getText().isEmpty() == true || TXTPhone.getText().isEmpty() == true)
        {
            JOptionPane.showMessageDialog(null, "Your Data Is Not Complete!!");
        }
        else
        {
            
			try {
				// TODO add your handling code here:
                                String GenerateID = AllClass.GenerateID("consumer", "idconsumer");
				Conn.rowSet.setCommand("select * from consumer");
				Conn.rowSet.execute();
				Conn.rowSet.moveToInsertRow();
                                Conn.rowSet.updateString("idconsumer", GenerateID);
				Conn.rowSet.updateString("name", TXTName.getText());
				Conn.rowSet.updateString("address", TXTAddress.getText());
				Conn.rowSet.updateString("phone", TXTPhone.getText());
                                Conn.rowSet.updateString("Type", TypeConsumen.getSelectedItem().toString());
				Conn.rowSet.insertRow();
				updateTable();
			} catch (SQLException ex) {
				Logger.getLogger(AdminUser.class.getName()).log(Level.SEVERE, null, ex);
			}
		}
        clear();            
        // TODO add your handling code here:
    }//GEN-LAST:event_AddButActionPerformed

