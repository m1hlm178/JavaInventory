    public void updateTable(){
		try {
                        DefaultTableModel Table = (DefaultTableModel) TableStore.getModel();
                        while(Table.getRowCount() > 0) Table.removeRow(0);
			Conn.rowSet.setCommand("select idconsumer , Name, address, phone, Type from consumer");
			Conn.rowSet.execute();
                        while(Conn.rowSet.next())
                        {
                            Table.addRow(new Object[]{Conn.rowSet.getString(1),Conn.rowSet.getString(2),Conn.rowSet.getString(3),Conn.rowSet.getString(4),Conn.rowSet.getString(5)});
                        }
		} catch (SQLException ex) {
			Logger.getLogger(AdminUser.class.getName()).log(Level.SEVERE, null, ex);
		}
    }
    
