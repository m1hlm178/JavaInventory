   private void prosesLogin() {
       try {			
			String sql = "select * from user where username ='" + txtUsername.getText() +"' AND password = md5('"+ txtPassword.getText() +"')";
			Conn.rowSet.setCommand(sql);
			Conn.rowSet.execute();

			while (Conn.rowSet.next()) {
				userID = Conn.rowSet.getString("idusername");
				userLogin = Conn.rowSet.getString("Name");
			}
			if (userLogin.equals("")) {
				JOptionPane.showMessageDialog(null, "USERNAME atau PASSWORD SALAH","ERROR!!",JOptionPane.ERROR_MESSAGE);
				clear();
                                LogLogin("Failed");
			} 
			else {
                                    LogLogin("Sukses");
                                    MenuUtama frm = new MenuUtama();
                                    FrameUtama.DekstopFrame.add(frm);
                                    frm.setVisible(true);
                                    frm.setMaximum(true);
                                    dispose();
                                    FrameUtama.UserStat.setText("Welcome, " + userLogin);
			}
//                        Conn.rowSet.close();
       } catch (Exception  e) {
           Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
       }
   }
    
