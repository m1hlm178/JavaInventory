    public void loadimage(){
        try
        {
            Conn.rowSet.setCommand("select * from store");
            Conn.rowSet.execute();
            while (Conn.rowSet.next()) {
                InputStream is = Conn.rowSet.getBinaryStream("Logo");
                Image bimg = ImageIO.read(is);
                bimg = bimg.getScaledInstance(285,124,Image.SCALE_SMOOTH);
                ImageIcon icon = new ImageIcon(bimg);
                ImageLogo.setIcon(icon);
            }
//            Conn.rowSet.close();
        }   catch(Exception e){
         e.printStackTrace();
        }}

